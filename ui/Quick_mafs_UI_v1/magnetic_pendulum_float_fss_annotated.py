def save_arrays(base, plus, minus, fss):
    """Save all arrays needed for hardware/software comparison."""

    np.save(OUT_DIR / "basin_base.npy", base["basin"])
    np.save(OUT_DIR / "basin_plus_epsilon.npy", plus["basin"])
    np.save(OUT_DIR / "basin_minus_epsilon.npy", minus["basin"])

    np.save(OUT_DIR / "steps_base.npy", base["steps"])
    np.save(OUT_DIR / "steps_plus_epsilon.npy", plus["steps"])
    np.save(OUT_DIR / "steps_minus_epsilon.npy", minus["steps"])

    np.save(OUT_DIR / "strictly_settled_base.npy", base["strictly_settled"])
    np.save(OUT_DIR / "strictly_settled_plus_epsilon.npy", plus["strictly_settled"])
    np.save(OUT_DIR / "strictly_settled_minus_epsilon.npy", minus["strictly_settled"])

    np.save(OUT_DIR / "fss_sensitive.npy", fss["sensitive"])
    np.save(OUT_DIR / "fss_timeout_any.npy", fss["timeout_any"])
    np.save(OUT_DIR / "fss_base_step_category.npy", fss["base_step_category"])
    np.save(OUT_DIR / "fss_word.npy", fss["fss_word"])

    np.savez_compressed(
        OUT_DIR / "fss_result_bundle.npz",
        basin_base=base["basin"],
        basin_plus=plus["basin"],
        basin_minus=minus["basin"],
        steps_base=base["steps"],
        fss_sensitive=fss["sensitive"],
        fss_timeout_any=fss["timeout_any"],
        fss_word=fss["fss_word"],
    )


def save_summary(base, plus, minus, fss):
    """Save a JSON summary of parameters and FSS statistics."""

    summary = {
        "resolution": [WIDTH, HEIGHT],
        "window": {
            "x_min": X_MIN,
            "x_max": X_MAX,
            "y_min": Y_MIN,
            "y_max": Y_MAX,
        },
        "physics": {
            "gamma": GAMMA,
            "omega2": OMEGA2,
            "h2": H2,
            "mu": MU,
        },
        "simulation": {
            "dt": DT,
            "max_steps": MAX_STEPS,
            "r_settle": R_SETTLE,
            "v_settle": V_SETTLE,
            "min_consecutive": MIN_CONSECUTIVE,
            "classify_on_timeout": CLASSIFY_ON_TIMEOUT,
        },
        "fss": {
            "epsilon": EPSILON,
            "definition": "sensitive = no_timeout and ((base != plus) or (base != minus))",
            "sensitive_count": fss["sensitive_count"],
            "sensitive_fraction": fss["f_epsilon"],
            "sensitive_percent": 100.0 * fss["f_epsilon"],
            "timeout_count": fss["timeout_count"],
            "timeout_fraction": fss["timeout_fraction"],
            "hardware_word_format": {
                "bits_5_2": "base trajectory step category",
                "bit_1": "timeout_any flag",
                "bit_0": "sensitive flag",
            },
        },
        "runtime_seconds": {
            "base": base["elapsed_s"],
            "plus_epsilon": plus["elapsed_s"],
            "minus_epsilon": minus["elapsed_s"],
            "total": base["elapsed_s"] + plus["elapsed_s"] + minus["elapsed_s"],
        },
    }

    with open(OUT_DIR / "summary.json", "w", encoding="utf-8") as f:
        json.dump(summary, f, indent=2)


# =============================================================================
# Main run
# =============================================================================


def main():
    OUT_DIR.mkdir(parents=True, exist_ok=True)

    print("Magnetic pendulum floating-point FSS reference")
    print("------------------------------------------------")
    print(f"resolution : {WIDTH} x {HEIGHT}")
    print(f"epsilon    : {EPSILON}")
    print(f"gamma      : {GAMMA}")
    print(f"output dir : {OUT_DIR.resolve()}")

    print("\nGenerating base basin map...")
    base = generate_basin_map(x_offset=0.0, name="base")

    print("\nGenerating plus-epsilon basin map...")
    plus = generate_basin_map(x_offset=EPSILON, name="plus")

    print("\nGenerating minus-epsilon basin map...")
    minus = generate_basin_map(x_offset=-EPSILON, name="minus")

    print("\nComputing FSS map...")
    fss = compute_fss(base, plus, minus)

    print("\nSaving arrays and plots...")
    save_arrays(base, plus, minus, fss)
    save_summary(base, plus, minus, fss)

    plt.imsave(OUT_DIR / "basin_base_raw.png", basin_to_rgb(base["basin"]))
    save_annotated_basin(base, OUT_DIR / "basin_base_annotated.png")
    save_annotated_fss_map(fss, OUT_DIR / "fss_map_annotated.png")
    save_step_heatmap(base, OUT_DIR / "step_base_annotated.png")

    print("\nFSS summary:")
    print(f"  sensitive pixels : {fss['sensitive_count']} / {WIDTH * HEIGHT}")
    print(f"  f_epsilon        : {fss['f_epsilon']:.6f}")
    print(f"  sensitive percent: {100.0 * fss['f_epsilon']:.2f}%")
    print(f"  timeout pixels   : {fss['timeout_count']} / {WIDTH * HEIGHT}")
    print("\nSaved files:")
    print(f"  {OUT_DIR / 'basin_base_annotated.png'}")
    print(f"  {OUT_DIR / 'fss_map_annotated.png'}")
    print(f"  {OUT_DIR / 'step_base_annotated.png'}")
    print(f"  {OUT_DIR / 'summary.json'}")


if __name__ == "__main__":
    main()
