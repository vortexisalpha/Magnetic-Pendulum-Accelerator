def basin_to_rgb(basin, timeout_mask=None, darken_timeout=False):
    """
    Convert integer basin labels into RGB colours.

    timeout pixels can optionally be darkened. The numerical arrays always keep
    timeout information separately, so this is only a visual choice.
    """

    rgb = np.zeros((basin.shape[0], basin.shape[1], 3), dtype=np.uint8)

    for label, colour in BASIN_COLOURS.items():
        rgb[basin == label] = np.array(colour, dtype=np.uint8)

    if darken_timeout and timeout_mask is not None:
        rgb[timeout_mask] = (0.45 * rgb[timeout_mask]).astype(np.uint8)

    return rgb


def save_annotated_basin(base, path):
    """
    Save an annotated basin-of-attraction plot.

    The output includes:
        - physical x/y axes,
        - magnet markers,
        - a colour legend,
        - a title with the main simulation parameters.
    """

    rgb = basin_to_rgb(base["basin"], base["timeout"], darken_timeout=False)

    fig, ax = plt.subplots(figsize=(8, 7))
    ax.imshow(
        rgb,
        origin="lower",
        extent=[X_MIN, X_MAX, Y_MIN, Y_MAX],
        interpolation="nearest",
    )

    for i, (mx, my) in enumerate(MAGNETS):
        ax.plot(mx, my, marker="+", color="white", markersize=14, markeredgewidth=2.5)
        ax.text(mx + 0.06, my + 0.06, f"M{i}", color="white", fontsize=11, weight="bold")

    legend_items = [
        Patch(facecolor=np.array(BASIN_COLOURS[0]) / 255.0, label="Magnet 0"),
        Patch(facecolor=np.array(BASIN_COLOURS[1]) / 255.0, label="Magnet 1"),
        Patch(facecolor=np.array(BASIN_COLOURS[2]) / 255.0, label="Magnet 2"),
    ]

    ax.legend(handles=legend_items, loc="upper right", framealpha=0.85)
    ax.set_title(
        "Basin of Attraction Map\n"
        f"resolution={WIDTH}x{HEIGHT}, gamma={GAMMA}, dt={DT}, max_steps={MAX_STEPS}"
    )
    ax.set_xlabel("initial x position, $x_0$")
    ax.set_ylabel("initial y position, $y_0$")

    timeout_count = int(np.sum(base["timeout"]))
    settled_percent = 100.0 * float(np.mean(base["strictly_settled"]))
    text = (
        f"strictly settled: {settled_percent:.2f}%\n"
        f"timeout-classified pixels: {timeout_count}"
    )
    ax.text(
        0.02,
        0.02,
        text,
        transform=ax.transAxes,
        color="white",
        fontsize=10,
        bbox=dict(facecolor="black", alpha=0.55, edgecolor="none"),
    )

    fig.tight_layout()
    fig.savefig(path, dpi=220, bbox_inches="tight")
    if SHOW_PLOTS:
        plt.show()
    plt.close(fig)


def save_annotated_fss_map(fss, path):
    """
    Save an annotated FSS map.

    Value convention for the displayed image:
        0 = stable
        1 = sensitive
        2 = timeout/invalid for FSS

    The saved numerical file fss_sensitive.npy stores only the Boolean sensitive
    map. The saved fss_timeout_any.npy stores timeout information separately.
    """

    display = np.zeros((HEIGHT, WIDTH), dtype=np.uint8)
    display[fss["sensitive"]] = 1
    display[fss["timeout_any"]] = 2

    cmap = ListedColormap(
        [
            (0.08, 0.10, 0.16),
            (1.00, 0.75, 0.12),
            (0.45, 0.45, 0.45),
        ]
    )

    fig, ax = plt.subplots(figsize=(8, 7))
    ax.imshow(
        display,
        origin="lower",
        extent=[X_MIN, X_MAX, Y_MIN, Y_MAX],
        interpolation="nearest",
        cmap=cmap,
        vmin=0,
        vmax=2,
    )

    for i, (mx, my) in enumerate(MAGNETS):
        ax.plot(mx, my, marker="+", color="white", markersize=14, markeredgewidth=2.5)
        ax.text(mx + 0.06, my + 0.06, f"M{i}", color="white", fontsize=11, weight="bold")

    legend_items = [
        Patch(facecolor=(0.08, 0.10, 0.16), label="Stable"),
        Patch(facecolor=(1.00, 0.75, 0.12), label="Sensitive"),
        Patch(facecolor=(0.45, 0.45, 0.45), label="Timeout / invalid"),
    ]

    ax.legend(handles=legend_items, loc="upper right", framealpha=0.85)
    ax.set_title(
        "Final-State Sensitivity Map\n"
        f"epsilon={EPSILON}, sensitive fraction={100.0 * fss['f_epsilon']:.2f}%"
    )
    ax.set_xlabel("initial x position, $x_0$")
    ax.set_ylabel("initial y position, $y_0$")

    text = (
        f"sensitive pixels: {fss['sensitive_count']} / {WIDTH * HEIGHT}\n"
        f"timeout/invalid pixels: {fss['timeout_count']}\n"
        "test: compare $A(x_0,y_0)$ with "
        "$A(x_0+\\epsilon,y_0)$ and $A(x_0-\\epsilon,y_0)$"
    )
    ax.text(
        0.02,
        0.02,
        text,
        transform=ax.transAxes,
        color="white",
        fontsize=10,
        bbox=dict(facecolor="black", alpha=0.60, edgecolor="none"),
    )

    fig.tight_layout()
    fig.savefig(path, dpi=220, bbox_inches="tight")
    if SHOW_PLOTS:
        plt.show()
    plt.close(fig)


def save_step_heatmap(base, path):
    """Save an annotated heatmap of the base trajectory step count."""

    fig, ax = plt.subplots(figsize=(8, 7))
    im = ax.imshow(
        base["steps"],
        origin="lower",
        extent=[X_MIN, X_MAX, Y_MIN, Y_MAX],
        interpolation="nearest",
        cmap="inferno",
    )

    for i, (mx, my) in enumerate(MAGNETS):
        ax.plot(mx, my, marker="+", color="white", markersize=12, markeredgewidth=2)
        ax.text(mx + 0.06, my + 0.06, f"M{i}", color="white", fontsize=10, weight="bold")

    fig.colorbar(im, ax=ax, label="integration steps")
    ax.set_title("Base Trajectory Step Count Heatmap")
    ax.set_xlabel("initial x position, $x_0$")
    ax.set_ylabel("initial y position, $y_0$")
    fig.tight_layout()
    fig.savefig(path, dpi=220, bbox_inches="tight")
    if SHOW_PLOTS:
        plt.show()
    plt.close(fig)


