"""
magnetic_pendulum_float_fss_annotated.py

Floating-point software reference with Final-State Sensitivity (FSS) analysis.

This script is intended to match the existing floating-point magnetic-pendulum
reference model and add the FSS function that mirrors the proposed hardware
extension.

The important rule is:

    Do not change the pendulum algorithm.

The same simplified force model, semi-implicit Euler update, nearest-magnet
classification, and consecutive settling rule are used for all three runs:

    base   : (x0, y0)
    plus   : (x0 + epsilon, y0)
    minus  : (x0 - epsilon, y0)

The FSS map is then computed as:

    sensitive = (base_label != plus_label) OR (base_label != minus_label)

If any of the three trajectories times out, the pixel is marked as timeout in
the FSS output and is not counted as sensitive. This matches the proposed
hardware format:

    bits [5:2] = base trajectory step category
    bit  [1]   = timeout_any flag
    bit  [0]   = sensitive flag

All parameters are set directly below. No command-line arguments are required.
"""

from pathlib import Path
import json
import time

import numpy as np
import matplotlib.pyplot as plt
from matplotlib.colors import ListedColormap
from matplotlib.patches import Patch


# =============================================================================
# User-editable parameters
# =============================================================================

WIDTH = 270
HEIGHT = 270

X_MIN = -1.8
X_MAX = 1.8
Y_MIN = -1.8
Y_MAX = 1.8

GAMMA = 0.20
OMEGA2 = 1.00
H2 = 0.25
MU = 1.00

DT = 0.01
MAX_STEPS = 4000
R_SETTLE = 0.25
V_SETTLE = 0.05
MIN_CONSECUTIVE = 3

EPSILON = 0.1
DTYPE = np.float64

CLASSIFY_ON_TIMEOUT = True
PROGRESS_INTERVAL = 500

OUT_DIR = Path("pendulum_outputs_fss_annotated")
SHOW_PLOTS = False

MAGNETS = np.array(
    [
        [1.0, 0.0],
        [-0.5, 0.8660254037844386],
        [-0.5, -0.8660254037844386],
    ],
    dtype=DTYPE,
)

BASIN_COLOURS = {
    0: (220, 60, 60),
    1: (60, 180, 60),
    2: (60, 60, 220),
    -1: (30, 30, 30),
}


# =============================================================================
# Grid and physics functions
# =============================================================================


def build_initial_grid(x_offset=0.0):
    """
    Build the initial-condition grid.

    Each image pixel represents one initial position (x0, y0). For FSS, the
    plus and minus runs are generated by adding an x_offset to the base grid.

    x_offset = 0.0      gives (x0, y0)
    x_offset = epsilon  gives (x0 + epsilon, y0)
    x_offset = -epsilon gives (x0 - epsilon, y0)

    No clipping is applied. If x0 + epsilon lies slightly outside the displayed
    physical window, it is still simulated as a valid perturbed coordinate.
    """

    xs = np.linspace(X_MIN, X_MAX, WIDTH, dtype=DTYPE) + DTYPE(x_offset)
    ys = np.linspace(Y_MIN, Y_MAX, HEIGHT, dtype=DTYPE)
    x, y = np.meshgrid(xs, ys, indexing="xy")
    return x, y


def acceleration(x, y, vx, vy):
    """
    Compute acceleration for the simplified magnetic-pendulum model.

    This is the same force model as the floating-point reference:

        ax = -gamma * vx - omega2 * x
             + mu * sum_i dx_i * q_i^(-3/2)

        ay = -gamma * vy - omega2 * y
             + mu * sum_i dy_i * q_i^(-3/2)

    where:

        dx_i = magnet_x_i - x
        dy_i = magnet_y_i - y
        q_i  = dx_i^2 + dy_i^2 + h2

    The q^(-3/2) term is the expensive term approximated by a LUT in hardware.
    """

    ax = -GAMMA * vx - OMEGA2 * x
    ay = -GAMMA * vy - OMEGA2 * y

    for mx, my in MAGNETS:
        dx = mx - x
        dy = my - y
        q = dx * dx + dy * dy + H2
        inv_q_3_2 = q ** (-1.5)
        ax = ax + MU * dx * inv_q_3_2
        ay = ay + MU * dy * inv_q_3_2

    return ax, ay


def nearest_magnet(x, y):
    """
    Return the nearest magnet ID and squared planar distance.

    The squared distance is used because only comparisons are needed. This is
    the same idea used in hardware to avoid a square-root operation.
    """

    distances = []

    for mx, my in MAGNETS:
        dx = mx - x
        dy = my - y
        distances.append(dx * dx + dy * dy)

    d2_stack = np.stack(distances, axis=0)
    label = np.argmin(d2_stack, axis=0).astype(np.int16)
    d2_min = np.min(d2_stack, axis=0)
    return label, d2_min


def step_category_from_steps(steps, timeout_mask):
    """
    Convert raw step counts into the same 4-bit step category used in hardware.

    Category 0 is reserved for timeout. Categories 1 to 11 represent increasing
    settling time ranges.
    """

    cat = np.zeros_like(steps, dtype=np.uint8)

    cat[(~timeout_mask) & (steps <= 500)] = 1
    cat[(~timeout_mask) & (steps > 500) & (steps <= 1000)] = 2
    cat[(~timeout_mask) & (steps > 1000) & (steps <= 1500)] = 3
    cat[(~timeout_mask) & (steps > 1500) & (steps <= 2000)] = 4
    cat[(~timeout_mask) & (steps > 2000) & (steps <= 2500)] = 5
    cat[(~timeout_mask) & (steps > 2500) & (steps <= 3000)] = 6
    cat[(~timeout_mask) & (steps > 3000) & (steps <= 3500)] = 7
    cat[(~timeout_mask) & (steps > 3500) & (steps <= 4000)] = 8
    cat[(~timeout_mask) & (steps > 4000) & (steps <= 4500)] = 9
    cat[(~timeout_mask) & (steps > 4500) & (steps <= 5000)] = 10
    cat[(~timeout_mask) & (steps > 5000)] = 11

    return cat


# =============================================================================
# Basin simulation
# =============================================================================


def generate_basin_map(x_offset=0.0, name="base"):
    """
    Generate one basin map for a given x-offset.

    This function is used three times for FSS:

        x_offset = 0.0       base trajectory
        x_offset = EPSILON   positive perturbation
        x_offset = -EPSILON  negative perturbation

    The algorithm is intentionally unchanged between the three runs. Only the
    initial x-coordinate is shifted.
    """

    x, y = build_initial_grid(x_offset=x_offset)
    vx = np.zeros_like(x)
    vy = np.zeros_like(y)

    basin = np.full((HEIGHT, WIDTH), -1, dtype=np.int16)
    steps = np.zeros((HEIGHT, WIDTH), dtype=np.int32)
    strictly_settled = np.zeros((HEIGHT, WIDTH), dtype=bool)
    active = np.ones((HEIGHT, WIDTH), dtype=bool)
    consecutive = np.zeros((HEIGHT, WIDTH), dtype=np.int16)

    r2_settle = R_SETTLE * R_SETTLE
    v2_settle = V_SETTLE * V_SETTLE

    t0 = time.perf_counter()
    last_step = 0

    for step in range(1, MAX_STEPS + 1):
        last_step = step

        ax, ay = acceleration(x, y, vx, vy)

        vx[active] = vx[active] + DT * ax[active]
        vy[active] = vy[active] + DT * ay[active]
        x[active] = x[active] + DT * vx[active]
        y[active] = y[active] + DT * vy[active]

        label, d2_min = nearest_magnet(x, y)
        speed2 = vx * vx + vy * vy

        settle_now = active & (d2_min < r2_settle) & (speed2 < v2_settle)
        consecutive[settle_now] += 1
        consecutive[active & ~settle_now] = 0

        done_now = active & (consecutive >= MIN_CONSECUTIVE)

        if np.any(done_now):
            basin[done_now] = label[done_now]
            steps[done_now] = step
            strictly_settled[done_now] = True
            active[done_now] = False

        if PROGRESS_INTERVAL > 0:
            should_print = (
                step == 1
                or step % PROGRESS_INTERVAL == 0
                or step == MAX_STEPS
                or not np.any(active)
            )
            if should_print:
                done_count = int(np.size(active) - np.sum(active))
                print(
                    f"{name:>6s} | step {step:5d}/{MAX_STEPS} | "
                    f"done {done_count:7d}/{WIDTH * HEIGHT}"
                )

        if not np.any(active):
            break

    if np.any(active):
        label, _ = nearest_magnet(x, y)
        if CLASSIFY_ON_TIMEOUT:
            basin[active] = label[active]
        else:
            basin[active] = -1
        steps[active] = MAX_STEPS

    elapsed_s = time.perf_counter() - t0

    return {
        "basin": basin,
        "steps": steps,
        "strictly_settled": strictly_settled,
        "timeout": ~strictly_settled,
        "elapsed_s": elapsed_s,
        "iterations_executed": last_step,
        "final_x": x,
        "final_y": y,
        "final_vx": vx,
        "final_vy": vy,
    }


# =============================================================================
# FSS computation
# =============================================================================


def compute_fss(base, plus, minus):
    """
    Compute the FSS map from three basin maps.

    A pixel is sensitive only if:

        1. none of the three trajectories timed out, and
        2. the final magnet of the plus or minus perturbation differs from base.

    timeout_any is stored separately because timeout means the FSS decision is
    not reliable for that pixel.
    """

    base_label = base["basin"]
    plus_label = plus["basin"]
    minus_label = minus["basin"]

    timeout_any = base["timeout"] | plus["timeout"] | minus["timeout"]

    sensitive = (
        (~timeout_any)
        & ((base_label != plus_label) | (base_label != minus_label))
    )

    base_step_cat = step_category_from_steps(base["steps"], base["timeout"])

    fss_word = (
        (base_step_cat.astype(np.uint8) << 2)
        | (timeout_any.astype(np.uint8) << 1)
        | sensitive.astype(np.uint8)
    )

    return {
        "sensitive": sensitive,
        "timeout_any": timeout_any,
        "base_step_category": base_step_cat,
        "fss_word": fss_word,
        "sensitive_count": int(np.sum(sensitive)),
        "timeout_count": int(np.sum(timeout_any)),
        "f_epsilon": float(np.mean(sensitive)),
        "timeout_fraction": float(np.mean(timeout_any)),
    }


